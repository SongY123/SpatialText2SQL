from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class ColumnDef:
    name: str
    data_type: str
    constraints: str = ""


@dataclass(frozen=True)
class TableDef:
    name: str
    description: str
    columns: tuple[ColumnDef, ...]
    indexes: tuple[str, ...] = ()


@dataclass(frozen=True)
class DatabaseSpec:
    id: str
    label: str
    description: str
    source_tables: tuple[str, ...]
    tables: tuple[TableDef, ...]
    join_paths: tuple[str, ...]
    query_context: dict[str, str]


def col(name: str, data_type: str, constraints: str = "") -> ColumnDef:
    return ColumnDef(name=name, data_type=data_type, constraints=constraints)


