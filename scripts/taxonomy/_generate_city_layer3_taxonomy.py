def main() -> None:
    out_dir = Path(__file__).resolve().parent
    urban_chapters: list[dict] = []
    for utax, title, labels in CHAPTERS:
        prefix = utax.replace("utax_", "")
        layer3 = []
        for i, zh in enumerate(labels, 1):
            layer3.append({"id": f"l3_{prefix}_{i:02d}", "label_zh": zh})
        urban_chapters.append({"id": utax, "title_zh": title, "layer3": layer3})

    doc = {
        "schema_version": "1.0",
        "description_zh": (
            "三层分类约定：第一层为城市代码；第二层为工程内 UN-GGIM 对齐的 ggim_1..ggim_14；"
            "第三层为按城市场景组织的精细化标签（utax_01..utax_14 章节，每章下列举 l3_xx_yy）。"
            "注意：utax 章节编号与 ggim_1..14 的 ISO 主题序号并不一一对应，需通过 ggim_to_urban_chapters.json 桥接。"
        ),
        "urban_chapters": urban_chapters,
    }
    path = out_dir / "city_ggim_layer3_taxonomy.json"
    path.write_text(json.dumps(doc, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {path}")


if __name__ == "__main__":
    main()
